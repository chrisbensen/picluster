import requests
import os
import socket
import json

def getEnvironmentVariable(name):
    if name in os.environ:
        return os.getenv(name)
    else:
        print("Error: environment variable {name} does not exist.".format(name = name))
        quit()

SERVER_IP = getEnvironmentVariable('SERVER_IP')

def addSwitch(switch_ip, ip):
    try:
        data = { 'switch_ip': switch_ip, 'ip': ip }
        headers = {'Content-type': 'application/json'}
        url = 'http://{}/addswitch'.format(SERVER_IP).rstrip()
        response = requests.post(url, data = json.dumps(data), headers = headers)
        message = response.json()
        if message["status"] == 'true':
          print("addSwitch succcess")
    except socket.error:
        print("error")


def setPort(ip, port):
    try:
        data = { 'ip': ip, 'port': port }
        headers = {'Content-type': 'application/json'}
        url = 'http://{}/setport'.format(SERVER_IP).rstrip()
        response = requests.post(url, data = json.dumps(data), headers = headers)
        message = response.json()
        if message["status"] == 'true':
          print("setPort succcess")
    except socket.error:
        print("error")

def setPiGroup(location, switch_ip):
    try:
        data = { 'location': location, 'switch_ip': switch_ip }
        headers = {'Content-type': 'application/json'}
        url = 'http://{}/setpigroup'.format(SERVER_IP).rstrip()
        response = requests.post(url, data = json.dumps(data), headers = headers)
        message = response.json()
        if message["status"] == 'true':
          print("setPiGroup succcess")
    except socket.error:
        print("error")

def register(location, switch_ip, ip, port):
  addSwitch(switch_ip, ip)
  setPort(ip, port)
  setPiGroup(switch_ip, location)



